.text

.global _start

_start:
	MOV R7, #4 @ OUTPUT TEXT
	MOV R0, #1 @ OUTPUT STREAM MONITOR
	MOV R2, #12 @ ALLOCATE EXACT SPACE FOR MESSAGE
	LDR R1, =MESSAGE @ LOAD MESSAGE
	SWI 0
	
end: @ AT THE END
	MOV R7, #1 @ EXIT TO TERMINAL 
	SWI 0 @ INTERRUPT FOREVER  

.data

MESSAGE: 
.ascii "Hello World\n"
